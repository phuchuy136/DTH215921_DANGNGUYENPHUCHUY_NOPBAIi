print("Câu hỏi: Trình bày ý nghĩa toán tử /, //, %, **, and, or, is.")
print("- Toán tử /: Chia bình thường")
print("- Toán tử //: Chia xong làm tròn xuống số nguyên gần nhất về phía âm vô cùng")
print("- Toán tử %: Chia lấy phần dư")
print("- Toán tử **: Toán tử luỹ thừa (a**b = a luỹ thừa b) ")
print("- Toán tử and: Là toán tử logic, nếu cả hai toán tử đều có giá trị khác 0 thì điều kiện true")
print("- Toán tử or: Một trong hai toán tử khác 0 thì điều kiện true")
print("- Toán tử is: So sánh vùng nhớ (a is b), điều kiện đúng nếu a và b cùng trỏ đến 1 đối tượng")